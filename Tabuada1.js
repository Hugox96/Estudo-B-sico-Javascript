// Para treinar as operações aritméticas básicas, vou criar uma tabuada simples.

// Para isso vou criar uma função que realize um for percorrendo de 0 a 10 com um número de minha escolha
function tabuada() {
    var n = 5;
    for(var i = 0; i<=15; i++){
        console.log(`${n} X ${i} = ${n*i}`);
    }
}

tabuada();
